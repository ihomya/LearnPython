print('hello,world') # 向屏幕上输出指定的文字‘hello,world’
# print()函数也可以接受多个字符串，用逗号“,”隔开，就可以连成一串输出
print('The quick brown fox','jumps over','the lazy dog')
# print()将会依次打印每个字符串，而遇到“,”将输出一个空格
print(300) # 也可打印整数
print(100 + 200) # 也可以打印计算结果
print('100 + 200 =',100 + 200) # 也可混合打印
name = input('Please enter your name') # 向程序中输入值，并将其赋值给name
# 根据用户输入的不同，输出结果也会不同
print('hello', name)